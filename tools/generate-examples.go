// Command generate-examples is retained for pipeline compatibility only.
//
// Terraform examples are now generated schema-driven, directly from the
// TerraformAttribute tree, by tools/generate-all-schemas.go (invoked via
// `make generate`). Generating them there — alongside the resource schemas
// they must match — guarantees examples can never drift from the schema.
// This command therefore performs no work.
package main

import "fmt"

func main() {
	fmt.Println("generate-examples: no-op. Examples are generated by " +
		"tools/generate-all-schemas.go (make generate), schema-driven, so they " +
		"cannot drift from the provider schema.")
}
